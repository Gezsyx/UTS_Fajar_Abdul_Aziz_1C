nilai_1  = int(input("Masukan Nilai 1 : "))
nilai_2 = int(input("Masukan Nilai 2 : "))

jumlah = nilai_1 + nilai_2
kurang = nilai_1 - nilai_2
kali = nilai_1 * nilai_2

print("="*100)
print(f"HASIL PENJUMLAH {nilai_1} + {nilai_2} = {jumlah}")
print(f"HASIL PENGURANGAN {nilai_1} - {nilai_2} = {kurang}")
print(f"HASIL PERKALIAN {nilai_1} x {nilai_2} = {kali}")